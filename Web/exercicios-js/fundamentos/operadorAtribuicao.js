const a = 7 // atribuição normal
let b = 3

b += a // atribuição aditiva (b = b + a)
console.log(b)

b -= 4 // atribuição subtrativa (b = b - 4)
console.log(b)

b *= 2 // atribuição multiplicativa (b = b * 2)
console.log(b)

b /= 2 // atribuição divisiva (b = b/2)
console.log(b)

b %= 2 // atribuição modular (b = b % 2) Se par = 0, se ímpar = 1
console.log(b)
